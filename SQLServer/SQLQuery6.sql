CREATE VIEW eef_mich
AS SELECT eef_2007_2017.* FROM eef_2007_2017 WHERE E03=16

SELECT * FROM eef_mich

CREATE VIEW totales(NOM_ENTIDAD, ANIO, TOTAL)
AS SELECT tc_entidad.NOM_ENTIDAD, eef_2007_2017.ANIO, SUM(eef_2007_2017.VAL_USD) AS TOTAL FROM eef_2007_2017
JOIN tc_entidad ON eef_2007_2017.E03 = tc_entidad.E03
GROUP BY  tc_entidad.NOM_ENTIDAD, eef_2007_2017.ANIO

SELECT * FROM totales

