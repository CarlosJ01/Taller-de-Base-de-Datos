SELECT * FROM eef_2007_2017
JOIN (SELECT eef_2007_2017.CODIGO_SCIAN ,eef_2007_2017.E03, COUNT(*) AS REPETICIONES FROM eef_2007_2017
	  GROUP BY eef_2007_2017.CODIGO_SCIAN ,eef_2007_2017.E03 HAVING COUNT(*) > 1) AS TABLA
ON eef_2007_2017.E03 = TABLA.E03 AND eef_2007_2017.CODIGO_SCIAN = TABLA.CODIGO_SCIAN
ORDER BY eef_2007_2017.E03 ASC